namespace ThemeController.Devices;

public class TableMod {
    public void BoardGame() {
        Console.WriteLine("Table top sinks to allow for board games to sit lower. Cupholders jut out from the table.");
    }
    
    public void ProjectedMap() {
        Console.WriteLine("Projector descends from the ceiling for maps.");
    }

    public void WarGame() {
        Console.WriteLine("Scenery alcoves are revealed on the walls. Table top is replaced with warzone map.");
    }
    
    public void SpeedFelt() {
        Console.WriteLine("Tabletopper suitable for poker or collectible card games appears on the table.");
    }

    public void Off() {
        Console.WriteLine("The table returns to being a normal table.");
    }
}