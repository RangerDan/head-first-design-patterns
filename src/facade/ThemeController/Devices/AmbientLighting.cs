namespace ThemeController.Devices;

public class AmbientLighting {
    public void BrightLighting() {
        Console.WriteLine("The lights in the room change to mimic bright daylight.");
    }
    
    public void Vaporwave() {
        Console.WriteLine("Pink and Teal lights cover the ceiling and floor.");
    }

    public void DankDungeon() {
        Console.WriteLine("Low lights flicker between red, orange, and yellow as if emitted from torches.");
    }
    
    public void AlienLair() {
        Console.WriteLine("A sickly green light covers the walls.");
    }

    public void Off() {
        Console.WriteLine("Specialized lighting is off. The room's fluorescents come on.");
    }
}